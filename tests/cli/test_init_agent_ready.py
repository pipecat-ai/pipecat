class TestBundledGuide:
    """The guide must ship and be release-clean (catches packaging + content regressions)."""

    def test_bundled_files_exist(self):
        assert (AGENT_TEMPLATES / "AGENTS.md").is_file()
        assert (AGENT_TEMPLATES / "CLAUDE.md").is_file()
        assert (AGENT_TEMPLATES / "GETTING_STARTED.md").is_file()

    def test_agents_content_is_release_clean(self):
        text = (AGENT_TEMPLATES / "AGENTS.md").read_text(encoding="utf-8")
        assert text.strip()
        assert "pipecat init" in text
        # No local-checkout paths or the removed command name should ever ship.
        assert "/Users/" not in text
        assert "pipecat create" not in text
        assert "TODO(release)" not in text

    def test_claude_is_agents_import(self):
        assert (AGENT_TEMPLATES / "CLAUDE.md").read_text(encoding="utf-8").strip() == "@AGENTS.md"

    def test_getting_started_is_release_clean(self):
        text = (AGENT_TEMPLATES / "GETTING_STARTED.md").read_text(encoding="utf-8")
        assert text.strip()
        # No local-checkout paths, and the template ships unstamped (the
        # provenance footer is appended at write time).
        assert "/Users/" not in text
        assert "Generated by" not in text
